package com.ccl.grandcanyon.types;

public class Theme {

  private int themeId;
  private String name;

  public Theme() {}

  public int getThemeId() {
    return themeId;
  }
  public void setThemeId(int themeId) {
    this.themeId = themeId;
  }
  public String getName() {
    return name;
  }
  public void setName(String name) {
    this.name = name;
  }
}
